package com.android.primaitech.siprima.Pembeli.Model;

public class Kunjungan_Pembeli_Model {
    String nama_karyawan, nama_pembeli, prospek, alamat_temu, kode_kunjungan, tanggal_pertemuan, status;

    public String getNama_pembeli() {
        return nama_pembeli;
    }

    public void setNama_pembeli(String nama_pembeli) {
        this.nama_pembeli = nama_pembeli;
    }

    public String getNama_karyawan() {
        return nama_karyawan;
    }

    public void setNama_karyawan(String nama_karyawan) {
        this.nama_karyawan = nama_karyawan;
    }

    public String getProspek() {
        return prospek;
    }

    public void setProspek(String prospek) {
        this.prospek = prospek;
    }

    public String getAlamat_temu() {
        return alamat_temu;
    }

    public void setAlamat_temu(String alamat_temu) {
        this.alamat_temu = alamat_temu;
    }

    public String getKode_kunjungan() {
        return kode_kunjungan;
    }

    public void setKode_kunjungan(String kode_kunjungan) {
        this.kode_kunjungan = kode_kunjungan;
    }

    public String getTanggal_pertemuan() {
        return tanggal_pertemuan;
    }

    public void setTanggal_pertemuan(String tanggal_pertemuan) {
        this.tanggal_pertemuan = tanggal_pertemuan;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
}
