package com.android.primaitech.siprima.Tipe_Rumah.Model;

public class Tipe_Rumah_Model {

    String kode_tipe, nama_tipe, biaya_konstruksi, hpp, harga_jual;

    public String getKode_tipe() {
        return kode_tipe;
    }

    public void setKode_tipe(String kode_tipe) {
        this.kode_tipe = kode_tipe;
    }

    public String getNama_tipe() {
        return nama_tipe;
    }

    public void setNama_tipe(String nama_tipe) {
        this.nama_tipe = nama_tipe;
    }

    public String getBiaya_konstruksi() {
        return biaya_konstruksi;
    }

    public void setBiaya_konstruksi(String biaya_konstruksi) {
        this.biaya_konstruksi = biaya_konstruksi;
    }

    public String getHpp() {
        return hpp;
    }

    public void setHpp(String hpp) {
        this.hpp = hpp;
    }

    public String getHarga_jual() {
        return harga_jual;
    }

    public void setHarga_jual(String harga_jual) {
        this.harga_jual = harga_jual;
    }
}
