package com.android.primaitech.siprima.Lahan.Model;

public class Pembayaran_Lahan_Model {
    String kode_pembayaran, jumlah_uang, create_at, keterangan, bukti_bayar, nama_rab, nama_proyek;

    public String getKode_pembayaran() {
        return kode_pembayaran;
    }

    public void setKode_pembayaran(String kode_pembayaran) {
        this.kode_pembayaran = kode_pembayaran;
    }

    public String getJumlah_uang() {
        return jumlah_uang;
    }

    public void setJumlah_uang(String jumlah_uang) {
        this.jumlah_uang = jumlah_uang;
    }

    public String getCreate_at() {
        return create_at;
    }

    public void setCreate_at(String create_at) {
        this.create_at = create_at;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

    public String getBukti_bayar() {
        return bukti_bayar;
    }

    public void setBukti_bayar(String bukti_bayar) {
        this.bukti_bayar = bukti_bayar;
    }

    public String getNama_rab() {
        return nama_rab;
    }

    public void setNama_rab(String nama_rab) {
        this.nama_rab = nama_rab;
    }

    public String getNama_proyek() {
        return nama_proyek;
    }

    public void setNama_proyek(String nama_proyek) {
        this.nama_proyek = nama_proyek;
    }
}
