package com.android.primaitech.siprima.Cuti.Model;

public class Cuti_Model {
    String kode_detail_cuti, status, keterangan, tanggal, nama_karyawan, nama_proyek, nama_unit;

    public String getKode_detail_cuti() {
        return kode_detail_cuti;
    }

    public void setKode_detail_cuti(String kode_detail_cuti) {
        this.kode_detail_cuti = kode_detail_cuti;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    public String getNama_karyawan() {
        return nama_karyawan;
    }

    public void setNama_karyawan(String nama_karyawan) {
        this.nama_karyawan = nama_karyawan;
    }

    public String getNama_proyek() {
        return nama_proyek;
    }

    public void setNama_proyek(String nama_proyek) {
        this.nama_proyek = nama_proyek;
    }

    public String getNama_unit() {
        return nama_unit;
    }

    public void setNama_unit(String nama_unit) {
        this.nama_unit = nama_unit;
    }
}
