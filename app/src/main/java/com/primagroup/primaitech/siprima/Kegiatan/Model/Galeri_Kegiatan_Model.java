package com.primagroup.primaitech.siprima.Kegiatan.Model;

public class Galeri_Kegiatan_Model {
    String kode_galery, foto_kecil, keterangan, tanggal, foto;
    int index;

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    public String getKode_galery() {
        return kode_galery;
    }

    public void setKode_galery(String kode_galery) {
        this.kode_galery = kode_galery;
    }

    public String getFoto_kecil() {
        return foto_kecil;
    }

    public void setFoto_kecil(String foto_kecil) {
        this.foto_kecil = foto_kecil;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

    public String getTanggal() {
        return tanggal;
    }

    public void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }
}
