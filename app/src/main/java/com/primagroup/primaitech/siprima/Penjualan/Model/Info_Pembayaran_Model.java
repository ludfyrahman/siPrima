package com.primagroup.primaitech.siprima.Penjualan.Model;

public class Info_Pembayaran_Model {
    String kode_jadwal, kode_penjualan, nama_pembayaran, jumlah_bayar, tanggal_bayar, angsuran_ke, status_bayar;

    public String getKode_jadwal() {
        return kode_jadwal;
    }

    public void setKode_jadwal(String kode_jadwal) {
        this.kode_jadwal = kode_jadwal;
    }

    public String getKode_penjualan() {
        return kode_penjualan;
    }

    public void setKode_penjualan(String kode_penjualan) {
        this.kode_penjualan = kode_penjualan;
    }

    public String getNama_pembayaran() {
        return nama_pembayaran;
    }

    public void setNama_pembayaran(String nama_pembayaran) {
        this.nama_pembayaran = nama_pembayaran;
    }

    public String getJumlah_bayar() {
        return jumlah_bayar;
    }

    public void setJumlah_bayar(String jumlah_bayar) {
        this.jumlah_bayar = jumlah_bayar;
    }

    public String getTanggal_bayar() {
        return tanggal_bayar;
    }

    public void setTanggal_bayar(String tanggal_bayar) {
        this.tanggal_bayar = tanggal_bayar;
    }

    public String getAngsuran_ke() {
        return angsuran_ke;
    }

    public void setAngsuran_ke(String angsuran_ke) {
        this.angsuran_ke = angsuran_ke;
    }

    public String getStatus_bayar() {
        return status_bayar;
    }

    public void setStatus_bayar(String status_bayar) {
        this.status_bayar = status_bayar;
    }
}
